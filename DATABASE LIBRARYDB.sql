USE LILBRARYDB;
CREATE TABLE BOOK( ID INT PRIMARY KEY ,
TITLE VARCHAR(100)  ,
AUTHOR VARCHAR(50)  ,
PUBLISHED_YEAR  INT,
GENRE VARCHAR(30)
);

CREATE TABLE MEMBER1(
ID INT PRIMARY KEY ,
NAME VARCHAR(50) ,
MEMBERSHIP DATE);

CREATE TABLE BORROWING1(
ID INT PRIMARY KEY ,
BOOK_ID INT,
MEMBER_ID INT,
BORROW DATE,
RETURNING DATE);


